// Префиксный вариант ++i:

let i = 0;
while (++i < 5) alert(i);

// Результаты вывода alert: 1, 2, 3, 4 последовательно


// Постфиксный вариант i++:

let i = 0;
while (i++ < 5) alert(i);

// Результаты вывода alert: 1, 2, 3, 4, 5 последовательно

// Данный пример указывает на различия, связанные с применением
// префиксной и постфиксной форм записи счётчика в сравнениях.